// Create a new contract instance
const contract = new ethers.Contract(contractAddress, abi, provider);

async function downloadImage(url, path) {
  const response = await axios.get(url, { responseType: "stream" });
  response.data.pipe(fs.createWriteStream(path));
  return new Promise((resolve, reject) => {
    response.data.on("end", () => resolve());
    response.data.on("error", () => reject());
  });
}

async function downloadNFTImages() {
  // This would need to be adapted to the specific NFT collection and how many items it contains
  for (let tokenId = 1; tokenId <= 10000; tokenId++) {
    try {
      const tokenURI = await contract.tokenURI(tokenId);
      const response = await axios.get(tokenURI);
      const imageURL = response.data.image; // This might need to be changed based on the NFT metadata
      await downloadImage(imageURL, "./images/${tokenId}.png"); // Or whatever format the images are in
    } catch (error) {
      console.error(
        `Failed to download image for token ID ${tokenId}: , error`
      );
    }
  }
}

downloadNFTImages();
